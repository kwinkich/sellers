import { z } from "zod";

export const createCaseSchema = z.object({
	title: z
		.string()
		.min(1, "Название кейса обязательно")
		.min(3, "Название кейса должно содержать минимум 3 символа"),
	recommendedSellerLevel: z
		.string()
		.min(1, "Выберите рекомендуемый уровень продавца"),
	situation: z
		.string()
		.min(1, "Описание ситуации обязательно")
		.min(10, "Описание ситуации должно содержать минимум 10 символов")
		.max(1000, "Описание ситуации не должно превышать 1000 символов"),
	sellerLegend: z
		.string()
		.min(1, "Легенда продавца обязательна")
		.min(10, "Легенда продавца должна содержать минимум 10 символов")
		.max(1000, "Легенда продавца не должна превышать 1000 символов"),
	buyerLegend: z
		.string()
		.min(1, "Легенда покупателя обязательна")
		.min(10, "Легенда покупателя должна содержать минимум 10 символов")
		.max(1000, "Легенда покупателя не должна превышать 1000 символов"),
	sellerTask: z
		.string()
		.min(1, "Задача продавца обязательна")
		.min(10, "Задача продавца должна содержать минимум 10 символов")
		.max(1000, "Задача продавца не должна превышать 1000 символов"),
	buyerTask: z
		.string()
		.min(1, "Задача покупателя обязательна")
		.min(10, "Задача покупателя должна содержать минимум 10 символов")
		.max(1000, "Задача покупателя не должна превышать 1000 символов"),
});

export type CreateCaseFormData = z.infer<typeof createCaseSchema>;